export async function createReservation(data: CreateReservationRequest): Promise<ReservationFromAPI> {
  console.log('[Reservations] Creating reservation for location:', data.location_id ?? data.restaurant_id, 'qty:', data.quantity);
  // Per-request 60s timeout override. The default apiClient timeout (15s) was
  // tripping on slow backends — the reservation endpoint does its business-
  // member notification fanout + badge queries inline (see backend
  // routes/reservations.js POST '/'), which can push the response past 15s
  // when an org has several members or the push-notif provider is sluggish.
  // The transaction commits server-side, so a client-side timeout produced
  // a misleading "no internet" error while the order had actually gone
  // through. 60s is generous enough to cover real-world fanout without
  // hiding a genuinely-stuck request forever. The companion recovery path
  // in reserve.tsx (onError → fetchMyReservations → match check) catches
  // the rare case where the timeout still fires.
  // Header takes precedence; body field is a redundant fallback so even an
  // intermediate layer that strips custom headers (some corporate proxies do)
  // still gets the dedup key through to the backend.
  const idempotencyKey = data.idempotency_key && data.idempotency_key.length > 0 ? data.idempotency_key : undefined;
  const res = await apiClient.post<ReservationFromAPI | { reservation: ReservationFromAPI } | { data: ReservationFromAPI }>('/api/reservations', {
    location_id: data.location_id,
    restaurant_id: data.restaurant_id,
    basket_id: data.basket_id,
    quantity: data.quantity,
    payment_method: data.payment_method,
    credit_amount: data.credit_amount,
    client_request_id: idempotencyKey,
  }, {
    timeout: 60_000,
    headers: idempotencyKey ? { 'Idempotency-Key': idempotencyKey } : undefined,
  });
  const resData = res.data;
  let reservation: ReservationFromAPI;
  if (resData && typeof resData === 'object' && 'reservation' in resData) {
    reservation = (resData as any).reservation;
  } else if (resData && typeof resData === 'object' && 'data' in resData) {
    reservation = (resData as any).data;
  } else {
    reservation = resData as ReservationFromAPI;
  }
  console.log('[Reservations] Created reservation:', reservation.id);
  return reservation;
}

export async function fetchMyReservations(): Promise<ReservationFromAPI[]> {
  console.log('[Reservations] Fetching my reservations');
  const res = await apiClient.get<ReservationFromAPI[] | { reservations: ReservationFromAPI[] } | { data: ReservationFromAPI[] }>('/api/reservations/my/reservations');
  const data = res.data;
  let reservations: ReservationFromAPI[];
  if (Array.isArray(data)) {
    reservations = data;
  } else if (data && typeof data === 'object' && 'reservations' in data && Array.isArray((data as any).reservations)) {
    reservations = (data as any).reservations;
  } else if (data && typeof data === 'object' && 'data' in data && Array.isArray((data as any).data)) {
    reservations = (data as any).data;
  } else {
    console.log('[Reservations] Unexpected response shape:', JSON.stringify(data).substring(0, 200));
    reservations = [];
  }
  console.log('[Reservations] Fetched', reservations.length, 'reservations');
  return reservations;
}

/**
 * Lightweight reservation summary used by the chat screen to render the
 * OrderSummaryCard at the top before any conversation row exists. The
 * backend GET /api/reservations/:id endpoint returns these fields in
 * addition to the standard reservation columns; the chat screen falls
 * back to them when conversation.res_* aren't populated yet.
 */
export interface ReservationSummary {
  id: number;
  quantity?: number;
  total?: number | string | null;
  basket_name?: string | null;
  basket_image_url?: string | null;
  restaurant_name?: string | null;
  org_logo_url?: string | null;
  /** Buyer's display name — populated for the business-side chat header
   *  before any conversation row exists for the reservation. */
  buyer_name?: string | null;
}

export async function fetchReservationById(reservationId: string | number): Promise<ReservationSummary | null> {
  try {
    const res = await apiClient.get<any>(`/api/reservations/${reservationId}`);
    return (res.data ?? null) as ReservationSummary | null;
  } catch {
    return null;
  }
}

export async function fetchReservationQRCode(reservationId: string): Promise<string> {
  console.log('[Reservations] Fetching QR code for:', reservationId);
  const res = await apiClient.get<{ qrCode: string } | { data: string } | string>(`/api/reservations/${reservationId}/qrcode`);
  const data = res.data;
  if (typeof data === 'string') return data;
  if (data && typeof data === 'object' && 'qrCode' in data) return (data as any).qrCode;
  if (data && typeof data === 'object' && 'data' in data) return (data as any).data;
  return '';
}

export async function cancelReservation(reservationId: string, reason?: string): Promise<void> {
  console.log('[Reservations] Cancelling reservation:', reservationId, reason ? `reason: ${reason}` : '');
  await apiClient.delete(`/api/reservations/${reservationId}`, reason ? { data: { reason } } : undefined);
  console.log('[Reservations] Cancelled reservation:', reservationId);
}

export async function dismissReviewPrompt(reservationId: string): Promise<void> {
  console.log('[Reservations] Dismissing review prompt for:', reservationId);
  await apiClient.put(`/api/reservations/${reservationId}/dismiss-review`);
  console.log('[Reservations] Dismissed review prompt:', reservationId);
}

export async function hideReservation(reservationId: string): Promise<void> {
  console.log('[Reservations] Hiding reservation:', reservationId);
  await apiClient.put(`/api/reservations/${reservationId}/hide`);
  console.log('[Reservations] Hidden reservation:', reservationId);
}
