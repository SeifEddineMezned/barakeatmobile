import { apiClient } from '@/src/lib/api';

export type PaymentMethod = 'cash' | 'card' | 'credits';

export interface CreateReservationRequest {
  restaurant_id?: number;
  location_id?: number;
  basket_id?: number;
  quantity: number;
  payment_method?: PaymentMethod;
  /**
   * Wallet credits (TND) applied as a partial discount. The remainder
   * (total − credit_amount) is funded by `payment_method` (cash/card). The
   * credit slice is returned to the partner in full at settlement.
   */
  credit_amount?: number;
  /**
   * Defer the server-side notification fan-out (buyer + every business
   * member) by this many milliseconds AFTER the reservation row is
   * committed. The reservation itself is still created immediately; only
   * the bell-row INSERTs and the OS push dispatches wait. Used by the
   * reserve flow to keep notifications from popping over the in-app
   * confirmation animation. Server clamps to [0, 15000] ms.
   */
  notification_delay_ms?: number;
  /**
   * Per-attempt UUID generated by reserve.tsx when the user hits Confirmer.
   * Sent as the `Idempotency-Key` header AND in the body as a fallback. If
   * the same key arrives twice (network retry, ghost-order recovery), the
   * backend returns the original reservation instead of creating a
   * duplicate. The key persists across retries of the SAME attempt and is
   * reset whenever the cart state changes (qty/credit/payment/basket).
   */
  idempotency_key?: string;
}

